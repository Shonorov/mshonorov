package ru.job4j.max;

/**
 * Max two.
 * @author MShonorov (shonorov@gmail.com)
 */
public class Max {

    /**
     * Max of two ints.
     * @param first
     * @param second
     * @return max value.
     */
    public int max(int first, int second) {
        return first > second ? first : second;
    }
}
